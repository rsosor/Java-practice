package com.rsosor.micro.designpatterns.commandpattern.ex2;

public class Tetris {

    public Tetris() {}

    public void turnLeft() {
        System.out.println("向左轉");
    }

    public void turnRight() {
        System.out.println("向右轉");
    }

    public void straightDown() {
        System.out.println("直接下降");
    }
}
